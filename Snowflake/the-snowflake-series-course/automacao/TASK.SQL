CREATE OR REPLACE SCHEMA MANAGE_DB.TASKS;

CREATE OR REPLACE TASK LOAD_RAW
	WAREHOUSE = COMPUTE_WH
    SCHEDULE = '1 MINUTE'
    COMMENT = 'HAHA'
    AS
    COPY INTO MEDIAS.YOUTUBE.STATISTICS_RAW
    FROM @MANAGE_DB.EXTERNAL_STAGES.SNOWFLAKE_SERIES
    file_format= MANAGE_DB.FILE_FORMATS.JSON_FMT
	;

SHOW TASKS;

SELECT * FROM MEDIAS.YOUTUBE.STATISTICS_RAW;

TRUNCATE TABLE MEDIAS.YOUTUBE.STATISTICS_RAW;

ALTER TASK LOAD_RAW RESUME;

ALTER TASK LOAD_RAW SUSPEND;
